package day1;

public class IncrementDecrement {

	public static void main(String[] args) {
		int a=1;
		int b= ++a;
		System.out.println(a);
		System.out.println(b);
		System.out.println("*************************");
		a=4;
	    b=a++;
	   System.out.println(a);
	   System.out.println(b);
	   System.out.println("**********************");
	   
	   a=3;
	   b=a--;
	   System.out.println(a);
	   System.out.println(b);
	   System.out.println("***********************");
	   
	   a=1;
	   b=--a;
	   System.out.println(a);
	   System.out.println(b);

	}

}
